    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        try {
            String nome = txtNome.getText();
            int quantidade = Integer.parseInt(txtQTD.getText());
            double valor = Double.parseDouble(txtValor.getText());
            String classe = jTextField4.getText();

            res = new Residuo(0, nome, quantidade, valor, classe);
            new ResiduoDAO().inserir(res);
            dispose();

            txtNome.setText("");
            txtQTD.setText("");
            txtValor.setText("");
            jTextField4.setText("");
            cbEmpresa.setSelectedIndex(0);

            if (res.getId() != 0) {
                JOptionPane.showMessageDialog(null, "Resíduo cadastrado! ID cadastrado é: " + res.getId());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Não foi possível cadastrar o resíduo: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastroActionPerformed

